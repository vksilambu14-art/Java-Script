console.log(20 != "20");//false because it checks only the value and not the data type.

console.log(20 !== "20");//True because it checks both value and data type.

console.log(5 < 10);//True because 5 is less than 10.

console.log(15 >= 20);//False because 15 is not greater than or equal to 20.

